package yunnex.game.bean;

public class Game {
    
    private String name;
    
    public Game() {
        super();
    }

    public Game(String name) {
        super();
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
